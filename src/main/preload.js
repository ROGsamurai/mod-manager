const { contextBridge, ipcRenderer } = require('electron');
contextBridge.exposeInMainWorld('api', {
  minimize: () => ipcRenderer.invoke('window:minimize'),
  maximize: () => ipcRenderer.invoke('window:maximize'),
  close: () => ipcRenderer.invoke('window:close'),
  detectGame: () => ipcRenderer.invoke('game:detect'),
  getGamePath: () => ipcRenderer.invoke('game:get-path'),
  setGamePath: (p) => ipcRenderer.invoke('game:set-path', p),
  launchGame: () => ipcRenderer.invoke('game:launch'),
  getBepInExStatus: () => ipcRenderer.invoke('game:bepinex'),
  bepinexHealthCheck: () => ipcRenderer.invoke('game:bepinex-health'),
  getStagedFiles: () => ipcRenderer.invoke('staging:list'),
  addToStaging: () => ipcRenderer.invoke('staging:add'),
  removeFromStaging: (f) => ipcRenderer.invoke('staging:remove', f),
  clearStaging: () => ipcRenderer.invoke('staging:clear'),
  peekArchive: (f) => ipcRenderer.invoke('staging:peek', f),
  detectTargets: () => ipcRenderer.invoke('staging:detect-targets'),
  getTargets: () => ipcRenderer.invoke('targets:list'),
  installMod: (filename, targetKey, modName, skipRemoval) => ipcRenderer.invoke('mods:install', filename, targetKey, modName, skipRemoval),
  getInstalledMods: () => ipcRenderer.invoke('mods:list'),
  uninstallMod: (id) => ipcRenderer.invoke('mods:uninstall', id),
  freshInstall: () => ipcRenderer.invoke('mods:fresh-install'),
  toggleMod: (id) => ipcRenderer.invoke('mods:toggle', id),
  getConflicts: () => ipcRenderer.invoke('mods:conflicts'),
  markCore: (id, isCore) => ipcRenderer.invoke('mods:mark-core', id, isCore),
  markCoreBulk: (ids, isCore) => ipcRenderer.invoke('mods:mark-core-bulk', ids, isCore),
  getProfiles: () => ipcRenderer.invoke('profiles:list'),
  getActiveProfile: () => ipcRenderer.invoke('profiles:active'),
  createProfile: (name) => ipcRenderer.invoke('profiles:create', name),
  activateProfile: (id) => ipcRenderer.invoke('profiles:activate', id),
  deleteProfile: (id) => ipcRenderer.invoke('profiles:delete', id),
  exportProfile: (id) => ipcRenderer.invoke('profiles:export', id),
  importProfile: () => ipcRenderer.invoke('profiles:import'),
  getConfigFiles: () => ipcRenderer.invoke('config:list'),
  readConfigFile: (f) => ipcRenderer.invoke('config:read', f),
  saveConfigValue: (f, s, k, v) => ipcRenderer.invoke('config:save', f, s, k, v),
  checkDependencies: () => ipcRenderer.invoke('mods:check-deps'),
  regroupAllMods: () => ipcRenderer.invoke('mods:regroup-all'),
  getModGroups: () => ipcRenderer.invoke('groups:list'),
  createModGroup: (name) => ipcRenderer.invoke('groups:create', name),
  deleteModGroup: (id) => ipcRenderer.invoke('groups:delete', id),
  renameModGroup: (id, name) => ipcRenderer.invoke('groups:rename', id, name),
  reorderModGroups: (ids) => ipcRenderer.invoke('groups:reorder', ids),
  getCollapsedGroups: () => ipcRenderer.invoke('groups:get-collapsed'),
  setCollapsedGroups: (ids) => ipcRenderer.invoke('groups:set-collapsed', ids),
  setModGroup: (modId, groupId) => ipcRenderer.invoke('groups:set-mod', modId, groupId),
  setModsGroup: (modIds, groupId) => ipcRenderer.invoke('groups:set-mods', modIds, groupId),
  getDeleteAfterInstall: () => ipcRenderer.invoke('settings:get-delete-after-install'),
  setDeleteAfterInstall: (val) => ipcRenderer.invoke('settings:set-delete-after-install', val),
  getTheme: () => ipcRenderer.invoke('settings:get-theme'),
  setTheme: (id) => ipcRenderer.invoke('settings:set-theme', id),
  getMinimizeToTray: () => ipcRenderer.invoke('settings:get-minimize-to-tray'),
  setMinimizeToTray: (val) => ipcRenderer.invoke('settings:set-minimize-to-tray', val),
  getMinimizeBtnToTray: () => ipcRenderer.invoke('settings:get-minimize-btn-to-tray'),
  setMinimizeBtnToTray: (val) => ipcRenderer.invoke('settings:set-minimize-btn-to-tray', val),
  openFolderDialog: () => ipcRenderer.invoke('dialog:open-folder'),
  openStagingFolder: () => ipcRenderer.invoke('dialog:open-staging'),
  openAppDataFolder: () => ipcRenderer.invoke('dialog:open-appdata'),
  openSavesFolder: () => ipcRenderer.invoke('dialog:open-saves'),
  openGameFolder: () => ipcRenderer.invoke('dialog:open-game'),
  openUrl: (url) => ipcRenderer.invoke('dialog:open-url', url),
  getLocale: () => ipcRenderer.invoke('app:get-locale'),
  onStagingChanged: (cb) => {
    ipcRenderer.removeAllListeners('staging:changed');
    ipcRenderer.on('staging:changed', (_, d) => { try { cb(d); } catch (e) { console.error('[staging:changed]', e); } });
  },
  onInstallProgress: (cb) => {
    ipcRenderer.removeAllListeners('install-progress');
    ipcRenderer.on('install-progress', (_, data) => { try { cb(data); } catch (e) { console.error('[install-progress]', e); } });
  },
});
